// This backend route is used to create a new delivery note.
router.route("/createdeliverynote").post((req, res) => {
  const deliveryId = req.body.deliveryId;
  const pOrderId = req.body.pOrderId;
  const supplierId = req.body.supplierId;
  const date = req.body.date;
  const status = req.body.status;
  const itemCode = req.body.itemCode;
  const itemName = req.body.itemName;
  const unitPrice = Number(req.body.unitPrice);
  const qty = Number(req.body.qty);
  const uom = req.body.uom;
  const siteMngId = req.body.siteMngId;
  const siteId = req.body.siteId;
  const location = req.body.location;

  // Create a new delivery note object
  const deliveryNote = new DeliveryNote({
    deliveryId,
    pOrderId,
    supplierId,
    date,
    status,
    itemCode,
    itemName,
    unitPrice,
    qty,
    uom,
    siteMngId,
    siteId,
    location,
  });

  // Save the delivery note object to the database
  deliveryNote
    .save()
    .then(() => {
      res.json("Delivery Note Added.");
    })
    .catch((err) => {
      console.log(err);
    });
});

