// This backend route is used to update a purchase order.
router.route("/updatepurchaseorder/:pOrderId").put(async (req, res) => {
  let pOrderId = "#" + req.params.pOrderId;
  const {
    itemCode,
    itemName,
    uom,
    date,
    dueDate,
    supplierId,
    siteMngId,
    siteId,
    location,
    status,
  } = req.body;
  const unitPrice = Number(req.body.unitPrice);
  const qty = Number(req.body.qty);
  const amount = Number(req.body.amount);

  // Updated purchase order object
  const purchaseOrder = {
    pOrderId,
    itemCode,
    itemName,
    unitPrice,
    qty,
    uom,
    amount,
    date,
    dueDate,
    supplierId,
    siteMngId,
    siteId,
    location,
    status,
  };

  // Update the purchase order in the database
  await PurchaseOrder.findOneAndUpdate({ pOrderId: pOrderId }, purchaseOrder)
    .then(() => {
      res.status(200).send({ status: "Purchase Order Updated" });
    })
    .catch((err) => {
      console.log(err);
      res.status(500).send({
        status: "Error with updating the purchase order",
        error: err.message,
      });
    });
});

